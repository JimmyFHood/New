                                                                        function checkCookie() {
                                                                            var logged_before = getCookie("logged_before");
                                                                            if (logged_before != "") {
                                                                                return false;
                                                                            } else {
                                                                                document.cookie = "logged_before=logged_before; expires=Thu, 18 Dec 2022 12:00:00 UTC; path=/";
